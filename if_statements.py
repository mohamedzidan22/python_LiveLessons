#Accept the first integer
number1 = int(input("Enter the first integer: "))

#accept the second integer
number2 = int(input ("Enter the second integer: "))

#if first = second
if number1 == number2 :
    print("the first number equals the second number")
    
#if first != second
if number1 != number2:
    print("the first number does not equal the second")
    
#if first > second
if number1 > number2:
    print("the first number is greater than the second")
    
#if first < second
if number1 < number2:
    print("the first number is less than the second")
    
#if first >= second
if number1 >= number2:
    print("the first number is greater than or equal the second")
    
#if first <= second
if number1 <= number2:
    print("the first number is less than or equal the second")